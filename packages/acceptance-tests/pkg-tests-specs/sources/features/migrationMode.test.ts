import {ppath, xfs} from '@yarnpkg/fslib';
import {tests}      from 'pkg-tests-core';

const LEGACY_LOCKFILE = [
  `# This file is generated by running "yarn install" inside your project.\n`,
  `# Manual changes might be lost - proceed with caution!\n`,
  `\n`,
  `__metadata:\n`,
  `  version: 8\n`,
  `  cacheKey: 0c0\n`,
  `\n`,
  `"no-deps@npm:^1.0.0":\n`,
  `  version: 1.0.0\n`,
  `  resolution: "no-deps@npm:1.0.0"\n`,
  `  checksum: 0c0/af041f19ffad13c40b220f0319b95436c0e886ddb630988d78e35eb3b4e3d64131f1e7e9efa5818a7f3307718e322220bda7676fcc202f1841e587aa079b7205\n`,
  `  languageName: node\n`,
  `  linkType: hard\n`,
].join(``);

describe(`Features`, () => {
  describe(`Migration Mode`, () => {
    tests.testIf(`migrationMode`, `it shouldn't upgrade the lockfile when performing an install on an older project`, makeTemporaryEnv({
      dependencies: {
        [`no-deps`]: `1.0.0`,
      },
    }, {
      enableMigrationMode: `1`,
    }, async ({path, run, source}) => {
      const sourceLockfilePath = ppath.join(path, `yarn.lock`);
      await xfs.writeFilePromise(sourceLockfilePath, LEGACY_LOCKFILE);

      await run(`install`);

      const contentPostInstall = await xfs.readFilePromise(sourceLockfilePath, `utf8`);
      expect(contentPostInstall).toEqual(LEGACY_LOCKFILE);
    }));

    tests.testIf(`migrationMode`, `it shouldn't update the cache folder when installing packages`, makeTemporaryEnv({
      dependencies: {
        [`no-deps`]: `1.0.0`,
      },
    }, {
      enableMigrationMode: `1`,
    }, async ({path, run, source, git}) => {
      const sourceLockfilePath = ppath.join(path, `yarn.lock`);
      await xfs.writeFilePromise(sourceLockfilePath, LEGACY_LOCKFILE);

      await xfs.appendFilePromise(ppath.join(path, `.gitignore`), `.yarn/global\n`);

      await git(`init`);
      await git(`add`, `.`);
      await git(`commit`, `-m`, `test`);

      await run(`install`);

      await expect(git(`status`)).resolves.toMatchObject({
        stdout: expect.stringContaining(`nothing to commit, working tree clean`),
      });

      await run(`add`, `one-fixed-dep`);

      await expect(git(`status`)).resolves.toMatchObject({
        stdout: expect.stringContaining(`nothing to commit, working tree clean`),
      });
    }));

    tests.testIf(`migrationMode`, `it should use the new resolution hook when in migration mode`, makeTemporaryEnv({
      dependencies: {
        [`no-deps`]: `1.0.0`,
      },
    }, {
      enableMigrationMode: `1`,
    }, async ({path, run, source, git}) => {
      const sourceLockfilePath = ppath.join(path, `yarn.lock`);
      await xfs.writeFilePromise(sourceLockfilePath, LEGACY_LOCKFILE);

      await run(`add`, `one-fixed-dep`);

      await expect(source(`require('one-fixed-dep')`)).resolves.toEqual({
        name: `one-fixed-dep`,
        version: `1.0.0`,
      });
    }));
  });
});
